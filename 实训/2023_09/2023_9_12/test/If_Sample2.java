public class If_Sample2 {
    public static void main(String[] args) {
        int n = 7;
        if (n % 2 != 0) {
            System.out.println("n是奇数");
            System.out.println("条件表达式返回值为真");
        } else {
            System.out.println("n不是奇数");
            System.out.println("条件表达式返回值为假");
        }
    }
}